let currentLanguage = DEFAULT_LANGUAGE;

function normalizeLanguage(code) {
  const normalized = String(code || DEFAULT_LANGUAGE).trim().toLowerCase();
  return LANGUAGE_OPTIONS.some((item) => item.code === normalized) ? normalized : DEFAULT_LANGUAGE;
}

function interpolate(template, values = {}) {
  return String(template).replace(/\{\{(\w+)\}\}/g, (_, key) => {
    const value = values[key];
    return value === undefined || value === null ? '' : String(value);
  });
}

function getMap(language = currentLanguage) {
  return TRANSLATIONS[normalizeLanguage(language)] || TRANSLATIONS[FALLBACK_LANGUAGE];
}

export function getCurrentLanguage() {
  return currentLanguage;
}

export function getLanguageMeta(language = currentLanguage) {
  return LANGUAGE_OPTIONS.find((item) => item.code === normalizeLanguage(language)) || LANGUAGE_OPTIONS[0];
}

export function getCurrentLocale() {
  return getLanguageMeta(currentLanguage).locale;
}

export function getLanguageOptions() {
  return LANGUAGE_OPTIONS.slice();
}

export function isRTL(language = currentLanguage) {
  return getLanguageMeta(language).dir === 'rtl';
}

export function t(key, values = {}) {
  const languageMap = getMap(currentLanguage);
  const englishMap = getMap(FALLBACK_LANGUAGE);
  const template = languageMap[key] ?? englishMap[key] ?? key;
  return interpolate(template, values);
}

export function tp(baseKey, count, values = {}) {
  const rule = new Intl.PluralRules(getCurrentLocale()).select(Number(count));
  const key = `${baseKey}.${rule}`;
  const fallbackKey = `${baseKey}.other`;
  const languageMap = getMap(currentLanguage);
  const englishMap = getMap(FALLBACK_LANGUAGE);
  const template = languageMap[key]
    ?? languageMap[fallbackKey]
    ?? englishMap[key]
    ?? englishMap[fallbackKey]
    ?? fallbackKey;

  return interpolate(template, { ...values, count });
}

export function translateDom(root = document) {
  root.querySelectorAll('[data-i18n]').forEach((node) => {
    node.textContent = t(node.dataset.i18n);
  });

  root.querySelectorAll('[data-i18n-placeholder]').forEach((node) => {
    node.setAttribute('placeholder', t(node.dataset.i18nPlaceholder));
  });

  root.querySelectorAll('[data-i18n-title]').forEach((node) => {
    node.setAttribute('title', t(node.dataset.i18nTitle));
  });

  root.querySelectorAll('[data-i18n-aria-label]').forEach((node) => {
    node.setAttribute('aria-label', t(node.dataset.i18nAriaLabel));
  });
}

export function applyLanguage(language) {
  currentLanguage = normalizeLanguage(language);

  document.documentElement.lang = currentLanguage;
  document.documentElement.dir = isRTL(currentLanguage) ? 'rtl' : 'ltr';
  document.body?.setAttribute('dir', document.documentElement.dir);
  document.body?.setAttribute('data-language', currentLanguage);

  const description = document.querySelector('meta[name="description"]');
  if (description) {
    description.setAttribute('content', t('app.description'));
  }

  document.title = t('app.title');
  translateDom(document);
  document.dispatchEvent(new CustomEvent('app-language-change', { detail: { language: currentLanguage } }));

  return currentLanguage;
}
